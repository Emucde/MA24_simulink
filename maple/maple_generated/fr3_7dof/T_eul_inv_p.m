function tmpreturn = T_eul_inv_p(Phi, Phi_p)
  m = [0 0 0; 0 0 0; 0 0 0;];
  t1 = Phi(1);
  t2 = sin(t1);
  t3 = Phi(2);
  t4 = sin(t3);
  t6 = Phi_p(1);
  t7 = cos(t3);
  t10 = cos(t1);
  t11 = Phi_p(2);
  t14 = t4 ^ 2;
  t15 = 0.1e1 / t14;
  t5 = t4 * t7 * t6;
  m(1,1) = (t11 * t10 + t2 * t5) * t15;
  m(1,2) = (-t5 * t10 + t2 * t11) * t15;
  m(1,3) = 0.0e0;
  m(2,1) = -t6 * t10;
  m(2,2) = -t2 * t6;
  m(2,3) = 0.0e0;
  t21 = t15 * t11 * t7;
  t24 = 0.1e1 / t4 * t6;
  m(3,1) = -t10 * t21 - t2 * t24;
  m(3,2) = t10 * t24 - t21 * t2;
  m(3,3) = 0.0e0;
  tmpreturn = m;
