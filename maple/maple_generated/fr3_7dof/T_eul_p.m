function tmpreturn = T_eul_p(Phi, Phi_p)
  m = [0 0 0; 0 0 0; 0 0 0;];
  m(1,1) = 0;
  t1 = Phi(1);
  t2 = cos(t1);
  t3 = Phi_p(1);
  m(1,2) = -(t3 * t2);
  t5 = sin(t1);
  t6 = Phi(2);
  t7 = sin(t6);
  t10 = cos(t6);
  t12 = Phi_p(2);
  t8 = t12 * t10;
  t11 = t3 * t7;
  m(1,3) = (-t11 * t5 + t8 * t2);
  m(2,1) = 0;
  m(2,2) = -(t5 * t3);
  m(2,3) = (t11 * t2 + t5 * t8);
  m(3,1) = 0;
  m(3,2) = 0;
  m(3,3) = -(t7 * t12);
  tmpreturn = m;
