function tmpreturn = coriolis_matrix(q, q_p, param)
  m = [0 0; 0 0;];
  t1 = param.l__1 * param.m2;
  t3 = sin(q(2));
  t4 = t3 * param.l__s2;
  t5 = q_p(2);
  m(1,1) = -t5 * t4 * t1;
  t10 = q_p(1);
  m(1,2) = -(t5 + t10) * param.m2 * param.l__s2 * param.l__1 * t3;
  m(2,1) = t10 * t4 * t1;
  m(2,2) = 0.0e0;
  tmpreturn = m;
