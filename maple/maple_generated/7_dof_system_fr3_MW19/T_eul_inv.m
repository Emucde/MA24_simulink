function tmpreturn = T_eul_inv(Phi)
  m = [0 0 0; 0 0 0; 0 0 0;];
  t1 = Phi(1);
  t2 = cos(t1);
  t3 = Phi(2);
  t4 = cos(t3);
  t6 = sin(t3);
  t7 = 0.1e1 / t6;
  t5 = t4 * t7;
  m(1,1) = -t5 * t2;
  t9 = sin(t1);
  m(1,2) = -t9 * t5;
  m(1,3) = 0.1e1;
  m(2,1) = -t9;
  m(2,2) = t2;
  m(2,3) = 0.0e0;
  m(3,1) = t2 * t7;
  m(3,2) = t7 * t9;
  m(3,3) = 0.0e0;
  tmpreturn = m;
