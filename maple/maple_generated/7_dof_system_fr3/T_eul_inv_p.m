function tmpreturn = T_eul_inv_p(Phi, Phi_p)
  m = [0 0 0; 0 0 0; 0 0 0;];
  t1 = Phi(1);
  t2 = sin(t1);
  t3 = Phi(2);
  t4 = cot(t3);
  t5 = t2 * t4;
  t6 = Phi_p(1);
  t8 = cos(t1);
  t9 = Phi_p(2);
  t11 = csc(t3);
  t12 = t11 ^ 2;
  t7 = t12 * t9;
  m(1,1) = t6 * t5 + t8 * t7;
  t14 = t4 * t8;
  m(1,2) = -t14 * t6 + t2 * t7;
  m(1,3) = 0.0e0;
  t18 = t8 * t6;
  m(2,1) = -t18;
  t19 = t6 * t2;
  m(2,2) = -t19;
  m(2,3) = 0.0e0;
  m(3,1) = -(t14 * t9 + t19) * t11;
  m(3,2) = (-t9 * t5 + t18) * t11;
  m(3,3) = 0.0e0;
  tmpreturn = m;
