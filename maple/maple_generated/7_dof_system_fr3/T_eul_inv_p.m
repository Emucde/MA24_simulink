function tmpreturn = T_eul_inv_p(Phi, Phi_p)
  m = [0 0 0; 0 0 0; 0 0 0;];
  t1 = Phi(1);
  t2 = sin(t1);
  t3 = Phi_p(1);
  t4 = t2 * t3;
  t5 = Phi(2);
  t6 = cot(t5);
  t8 = cos(t1);
  t9 = Phi_p(2);
  t11 = csc(t5);
  t12 = t11 ^ 2;
  m(1,1) = t12 * t8 * t9 + t4 * t6;
  t14 = t3 * t8;
  m(1,2) = t12 * t2 * t9 - t14 * t6;
  m(1,3) = 0.0e0;
  m(2,1) = -t14;
  m(2,2) = -t4;
  m(2,3) = 0.0e0;
  m(3,1) = -(t6 * t8 * t9 + t4) * t11;
  m(3,2) = (-t2 * t6 * t9 + t14) * t11;
  m(3,3) = 0.0e0;
  tmpreturn = m;
