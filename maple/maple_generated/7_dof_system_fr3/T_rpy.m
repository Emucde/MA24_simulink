function tmpreturn = T_rpy(Phi)
  m = [0 0 0; 0 0 0; 0 0 0;];
  m(1,1) = 0;
  t1 = Phi(1);
  t2 = sin(t1);
  m(1,2) = -t2;
  t3 = cos(t1);
  t4 = Phi(2);
  t5 = cos(t4);
  m(1,3) = (t5 * t3);
  m(2,1) = 0;
  m(2,2) = t3;
  m(2,3) = (t5 * t2);
  m(3,1) = 1;
  m(3,2) = 0;
  t6 = sin(t4);
  m(3,3) = -t6;
  tmpreturn = m;
