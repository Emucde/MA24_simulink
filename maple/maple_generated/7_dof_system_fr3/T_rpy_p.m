function tmpreturn = T_rpy_p(Phi, Phi_p)
  m = [0 0 0; 0 0 0; 0 0 0;];
  m(1,1) = 0;
  t1 = Phi(1);
  t2 = cos(t1);
  t3 = Phi_p(1);
  m(1,2) = -(t3 * t2);
  t5 = sin(t1);
  t6 = Phi(2);
  t7 = cos(t6);
  t10 = sin(t6);
  t12 = Phi_p(2);
  t8 = t10 * t12;
  t11 = t7 * t3;
  m(1,3) = (-t11 * t5 - t2 * t8);
  m(2,1) = 0;
  m(2,2) = -(t5 * t3);
  m(2,3) = (t2 * t11 - t5 * t8);
  m(3,1) = 0;
  m(3,2) = 0;
  m(3,3) = -(t12 * t7);
  tmpreturn = m;
