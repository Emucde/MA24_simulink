function tmpreturn = T_eul_inv(Phi)
  m = [0 0 0; 0 0 0; 0 0 0;];
  t1 = Phi(1);
  t2 = cos(t1);
  t3 = Phi(2);
  t4 = cot(t3);
  m(1,1) = -t4 * t2;
  t6 = sin(t1);
  m(1,2) = -t6 * t4;
  m(1,3) = 0.1e1;
  m(2,1) = -t6;
  m(2,2) = t2;
  m(2,3) = 0.0e0;
  t8 = csc(t3);
  m(3,1) = t2 * t8;
  m(3,2) = t6 * t8;
  m(3,3) = 0.0e0;
  tmpreturn = m;
