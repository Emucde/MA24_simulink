function tmpreturn = inertia_matrix_inv_casadi(q, param)
  m = casadi.MX(2,2);
  t1 = param.l__s2 ^ 2;
  t2 = t1 * param.m2;
  t4 = q(2);
  t5 = sin(t4);
  t6 = t5 ^ 2;
  t7 = param.l__1 ^ 2;
  t9 = param.m2 ^ 2;
  t12 = param.l__s1 ^ 2;
  t13 = t12 * param.m1;
  t14 = t13 + param.I1;
  t21 = 0.1e1 / (t9 * t1 * t7 * t6 + param.m2 * (param.I2 * t7 + t14 * t1) + t14 * param.I2);
  m(1,1) = t21 * (t2 + param.I2);
  t22 = cos(t4);
  t25 = param.l__s2 * param.m2 * param.l__1 * t22;
  m(1,2) = t21 * (-t25 - t2 - param.I2);
  m(2,1) = m(1,2);
  m(2,2) = t21 * (0.2e1 * t25 + param.m2 * (t7 + t1) + t13 + param.I1 + param.I2);
  tmpreturn = m;
