function tmpreturn = inertia_matrix_casadi(q, param)
  m = casadi.SX(2,2);
  t1 = param.l__s1 ^ 2;
  t5 = cos(q(2));
  t8 = param.l__1 ^ 2;
  t9 = param.l__s2 ^ 2;
  m(1,1) = t1 * param.m1 + (0.2e1 * param.l__1 * param.l__s2 * t5 + t8 + t9) * param.m2 + param.I1 + param.I2;
  m(1,2) = (param.l__1 * t5 + param.l__s2) * param.l__s2 * param.m2 + param.I2;
  m(2,1) = m(1,2);
  m(2,2) = param.m2 * t9 + param.I2;
  tmpreturn = m;
