function tmpreturn = hom_transform_endeffector_casadi(q, param)
  m = casadi.SX(4,4);
  t1 = q(1);
  t2 = cos(t1);
  t3 = q(2);
  t4 = cos(t3);
  t6 = sin(t1);
  t7 = sin(t3);
  t8 = t6 * t7;
  m(1,1) = t2 * t4 - t8;
  t9 = t7 * t2;
  m(1,2) = -t6 * t4 - t9;
  m(1,3) = 0.0e0;
  t12 = param.l__2 * t4 + param.l__1;
  m(1,4) = -param.l__2 * t8 + t2 * t12;
  m(2,1) = -m(1,2);
  m(2,2) = m(1,1);
  m(2,3) = 0.0e0;
  m(2,4) = param.l__2 * t9 + t12 * t6 + param.l__I;
  m(3,1) = 0.0e0;
  m(3,2) = 0.0e0;
  m(3,3) = 0.1e1;
  m(3,4) = 0.0e0;
  m(4,1) = 0.0e0;
  m(4,2) = 0.0e0;
  m(4,3) = 0.0e0;
  m(4,4) = 0.1e1;
  tmpreturn = m;
