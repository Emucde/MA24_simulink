function tmpreturn = geo_jacobian_endeffector_casadi_SX(q, param)
  m = casadi.SX(2,6);
  t1 = q(1);
  t2 = cos(t1);
  t3 = q(2);
  t4 = sin(t3);
  t6 = sin(t1);
  t7 = cos(t3);
  t11 = 0.2e1 * param.l__2 * (-t4 * t2 - t7 * t6);
  m(1,1) = -param.l__1 * t6 + t11;
  t17 = 0.2e1 * param.l__2 * (t7 * t2 - t4 * t6);
  m(1,2) = param.l__1 * t2 + t17;
  m(1,3) = 0.0e0;
  m(1,4) = 0.0e0;
  m(1,5) = 0.0e0;
  m(1,6) = 0.1e1;
  m(2,1) = t11;
  m(2,2) = t17;
  m(2,3) = 0.0e0;
  m(2,4) = 0.0e0;
  m(2,5) = 0.0e0;
  m(2,6) = 0.1e1;
  tmpreturn = m';
