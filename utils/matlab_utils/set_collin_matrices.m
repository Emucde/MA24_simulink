function [R, K] = set_collin_matrices(R_J_d, K_J, param_robot)
    %given:
    % R_J_d = struct( ...
    % 'r21', 0, ...
    % 'r31', 0, 'r32', 0, ...
    % 'r41', 0, 'r42', 0, 'r43', 0, ...
    % 'r51', 0, 'r52', 0, 'r53', 0, 'r54', 0, ...
    % 'r61', 0, 'r62', 0, 'r63', 0, 'r64', 0, 'r65', 0 ...
    % );
    % K_J = struct(... 
    %     'k21', 0, ...
    %     'k31', 0, 'k32', 0, ...
    %     'k41', 0, 'k42', 0, 'k43', 0, ...
    %     'k51', 0, 'k52', 0, 'k53', 0, 'k54', 0, ...
    %     'k61', 0, 'k62', 0, 'k63', 0, 'k64', 0, 'k65', 0 ...
    % );

    n = param_robot.n_red;
    R = eye(n);
    K = zeros(n);
    for i = 2:n
        for j = 1:i-1
            R(i,j) = R_J_d.("r"+i+""+j);
            R(j,i) = R(i,j);
            K(i,j) = K_J.("k"+i+""+j);
            K(j,i) = K(i,j);
        end
    end
end